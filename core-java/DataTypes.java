class DataTypes{
    public static void main(String[] args){
        int i=10;
        float f=10.5f;
        double d=20.5;
        char c='A';
        boolean b=true;
        System.out.println(i);
        System.out.println(f);
        System.out.println(d);
        System.out.println(c);
        System.out.println(b);
    }
}